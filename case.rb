weather_today = 'snowy'

case weather_today
when 'snowy'
	puts "Make sure to use a special snow shoes."
when 'rainy'
	puts "Don't forget the umbrella at home."
when 'sunny'
  puts "Don't forget to use sunscreen."
when 'windy'
  puts 'Protect yourself from the strong windy.'
when 'cloudy'
  puts 'Remember to take a sweater.'
else
  puts "I don't know what place in this world do you live!!"
end
